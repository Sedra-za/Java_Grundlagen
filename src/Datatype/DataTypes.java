package Datatype;

public class DataTypes {
    public static void main(String[] args) {
        // byte: -128 to 127
        byte v1 = 10;

        // short: -32,768 to 32,767
        short v2 = 10000;

        // int: -2,147,483,648 to 2,147,483,647
        int v3 = 1000000;

        // long: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        long v4 = 3147483647L;

        // float
        float v5 = 1.5f;

        // double
        double v6 = 100.527;

        // boolean
        boolean v7 = true;
        boolean v8 = false;

        // char
        char v9 = 'a';
        char v10 = '#';

        // String
        String v11 = "Welcome in Java course";


        System.out.println("Data types in java");
        System.out.println("byte: " + v1);
        System.out.println("short: " + v2);
        System.out.println("int: " + v3);
        System.out.println("long: " + v4);
        System.out.println("float: " + v5);
        System.out.println("double: " + v6);
        System.out.println("boolean: " + v7);
        System.out.println("boolean: " + v8);
        System.out.println("char: " + v9);
        System.out.println("char: " + v10);
        System.out.println("String: " + v11);



    }
}